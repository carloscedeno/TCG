import base64
import json

def decode_jwt(token):
    try:
        _, payload_b64, _ = token.split('.')
        # Fix padding
        payload_b64 += '=' * (-len(payload_b64) % 4)
        payload = base64.b64decode(payload_b64).decode('utf-8')
        return json.loads(payload)
    except Exception as e:
        return {"error": str(e)}

